def normalize_hs(code):
    digits = re.sub(r"\D", "", code)
    return digits if len(digits) >= 2 else None

def extract_duty_vat(cell_text):
    percentages = re.findall(r"(\d+(?:\.\d+)?)%", cell_text)
    duty = float(percentages[0]) if percentages else 0.0
    vat = float(percentages[1]) if len(percentages) > 1 else 19.0
    return duty, vat

