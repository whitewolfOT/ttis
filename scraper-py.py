def get_hs_ancestors(hs_code: str):
    digits = re.sub(r"\D", "", hs_code)
    # Include the full code plus standard prefix lengths
    candidates = [digits] + [digits[:l] for l in (10, 8, 6, 4, 2) if len(digits) > l]
    # Remove duplicates while preserving order
    seen = set()
    result = []
    for c in candidates:
        if c not in seen and len(c) >= 2:
            seen.add(c)
            result.append(c)
    return result

def enumerate_paths(conn, hs_code, origin_country):
    ancestors = get_hs_ancestors(hs_code)
    if not ancestors:
        return []
    placeholders = ','.join(['?'] * len(ancestors))
    query = f"""
        SELECT hs_code, origin_country, duty_type, tax_type, rate,
               agreement_name, LENGTH(hs_code) as specificity
        FROM tariff_measures
        WHERE tax_type = 'DD'
          AND hs_code IN ({placeholders})
          AND (origin_country = ? OR origin_country = 'TN')
          AND (valid_from <= date('now') OR valid_from IS NULL)
          AND (valid_to >= date('now') OR valid_to IS NULL)
        ORDER BY
            CASE duty_type WHEN 'SUSP' THEN 0 WHEN 'PREF' THEN 1 WHEN 'QUOTA' THEN 2 ELSE 3 END,
            specificity DESC
    """
    cursor = conn.cursor()
    cursor.execute(query, ancestors + [origin_country])
    rows = cursor.fetchall()
    result = []
    for i, r in enumerate(rows):
        result.append({
            'rank': i+1,
            'hs_code': r[0],
            'origin_country': r[1],
            'duty_type': r[2],
            'rate': r[4],
            'agreement_name': r[5],
            'specificity': r[6]
        })
    return result

def resolve_duty(conn, hs_code, origin_country):
    paths = enumerate_paths(conn, hs_code, origin_country)
    if not paths:
        return {'rate': None, 'duty_type': 'NOT_FOUND'}
    return paths[0]

