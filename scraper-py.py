def fetch_chapter_hs_codes(chap):
    """Fetch list of HS codes for a chapter. Returns list of {hs_code, description}."""
    r = SESSION.get(BASE_URL, params={"choix": "1", "chap": chap}, timeout=30)
    soup = BeautifulSoup(r.text, "lxml")
    results = []
    for table in soup.find_all("table"):
        rows = table.find_all("tr")
        for tr in rows[1:]:  # skip header
            cells = tr.find_all(["td","th"])
            if len(cells) >= 2:
                hs = cells[0].get_text(" ", strip=True)
                desc = cells[1].get_text(" ", strip=True)
                hs_clean = re.sub(r"\D", "", hs)
                if len(hs_clean) >= 6:
                    results.append({"hs_code": hs_clean, "description": desc[:300]})
    return results

def fetch_hs_detail(hs_code):
    """Fetch full duty breakdown for one HS code. Returns dict with rates."""
    r = SESSION.get(BASE_URL, params={"choix": "", "chap": "", "sel": hs_code}, timeout=30)
    soup = BeautifulSoup(r.text, "lxml")

    result = {
        "hs_code": hs_code,
        "dd_rate": 0.0,
        "dd_assiette": "",
        "tva_rate": 19.0,
        "other_taxes": [],
        "pref_rates": [],
        "import_regime": "",
        "export_regime": "",
    }

    tables = soup.find_all("table")
    for table in tables:
        rows = table.find_all("tr")
        if not rows:
            continue
        headers = [th.get_text(" ", strip=True) for th in rows[0].find_all(["td","th"])]

        # Import/Export regime table
        if "IMPORT" in headers and "EXPORT" in headers:
            if len(rows) > 1:
                cells = rows[1].find_all(["td","th"])
                if len(cells) >= 2:
                    result["import_regime"] = cells[0].get_text(" ", strip=True)
                    result["export_regime"] = cells[1].get_text(" ", strip=True)

        # Duties table
        if "DROITS / TAXES" in headers and "Valeur" in headers:
            for tr in rows[1:]:
                cells = tr.find_all(["td","th"])
                if len(cells) >= 2:
                    tax_name = cells[0].get_text(" ", strip=True)
                    tax_val = cells[1].get_text(" ", strip=True)
                    assiette = cells[2].get_text(" ", strip=True) if len(cells) > 2 else ""

                    # Extract numeric rate
                    pct = re.findall(r"(\d+(?:\.\d+)?)\s*%", tax_val)
                    rate = float(pct[0]) if pct else None

                    if "DD" in tax_name and "DROIT DOUANE" in tax_name:
                        result["dd_rate"] = rate if rate is not None else 0.0
                        result["dd_assiette"] = assiette
                    elif "TVA" in tax_name:
                        result["tva_rate"] = rate if rate is not None else 19.0
                    else:
                        result["other_taxes"].append({
                            "name": tax_name, "value": tax_val, "assiette": assiette
                        })

        # Preferential rates table
        if "Taux Préférentiel" in headers or "Code Pays" in headers:
            for tr in rows[1:]:
                cells = tr.find_all(["td","th"])
                if len(cells) >= 3:
                    country_code = cells[0].get_text(" ", strip=True)
                    country_name = cells[1].get_text(" ", strip=True)
                    pref_val = cells[2].get_text(" ", strip=True)
                    pct = re.findall(r"(\d+(?:\.\d+)?)\s*%", pref_val)
                    pref_rate = float(pct[0]) if pct else None
                    if pref_rate is not None:
                        result["pref_rates"].append({
                            "country_code": country_code,
                            "country_name": country_name,
                            "rate": pref_rate
                        })

    return result

